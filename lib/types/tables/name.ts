/**
 * 名前レコード
 */
export interface NameRecord {
	/**
	 * プラットフォームID
	 */
	platformID: number;

	/**
	 * プラットフォーム固有のエンコーディングID
	 */
	encodingID: number;

	/**
	 * 言語ID
	 */
	languageID: number;

	/**
	 * 名前ID
	 */
	nameID: number;

	/**
	 * 文字列の長さ (バイト単位)
	 */
	length: number;

	/**
	 * 文字列データへのオフセット (文字列ストレージの先頭から)
	 */
	offset: number;

	/**
	 * デコードされた文字列
	 */
	string: string;
}

/**
 * 言語タグレコード (バージョン1の場合のみ)
 */
export interface LangTagRecord {
	/**
	 * 言語タグの長さ (バイト単位)
	 */
	length: number;

	/**
	 * 言語タグ文字列へのオフセット (言語タグストレージの先頭から)
	 */
	offset: number;

	/**
	 * 言語タグ文字列
	 */
	tag: string;
}

/**
 * nameテーブルの詳細情報
 */
export interface NameTable {
	/**
	 * nameテーブルのフォーマット (0 or 1)
	 */
	format: number;

	/**
	 * 名前レコードの数
	 */
	count: number;

	/**
	 * オフセット (文字列ストレージの先頭)
	 */
	stringOffset: number;

	/**
	 * 名前レコードの配列
	 */
	nameRecords: NameRecord[];

	/**
	 * 言語タグレコードの数 (フォーマット1の場合のみ)
	 */
	langTagCount?: number;

	/**
	 * 言語タグレコードの配列 (フォーマット1の場合のみ)
	 */
	langTagRecords?: LangTagRecord[];
}
